#include "../includes/fillit.h"

void	ft_valid_char(int *parse, char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if ((str[i] != FILL) && (str[i] != EMPTY) \
			&& (str[i] != '\n'))
			*parse = 1;
		i++;
	}
}
